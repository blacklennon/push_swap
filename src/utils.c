#include "push_swap.h"

void	print_lists(t_node *a, t_node *b)
{
	printf(" a | b \n=======\n");
	while (a || b)
	{
		printf("%2d | %-2d\n", a ? a->data : 0, b ? b->data : 0);
		if (a)
			a = a->next;
		if (b)
			b = b->next;
	}
}
